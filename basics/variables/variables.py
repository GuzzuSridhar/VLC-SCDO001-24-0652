# comments in python

# variable to store Strings (text)
name = "James Bond"
city = "Singapore"

print(name)
print(city)

# variable to store whole number types
age = 45
quantity = 10

print(age)
print(quantity)

# variables for numbers with decimal points
price = 10.43
distance = 4.2

print(price)
print(distance)

# variable for the boolean types (True or False)
is_valid = True
is_loggedin = False

print(is_valid)
print(is_loggedin)