#decimals
# binary numbers -- base 2 (0 and 1)
# octal numbers -- base 8 (0 and 7)
# headecimal numbers -- base 16 ( 0 and 9 , a and f)

binary_num = 0b1010       # Binary (10 in decimal)
octal_num = 0o12          # Octal (10 in decimal)
hex_num = 0xA             # Hexadecimal (10 in decimal)

# print the decimal equivalents
print(binary_num)
print(octal_num)
print(hex_num)

# convert decimal to binary
dec = 42
print(bin(dec))
print(oct(dec))
print(hex(dec))


