# float(x): Converts x to a float. It works with integers and strings containing numeric values.
# If x is a string, it must represent a valid number, or it raises an error.

# int to float
float_val = float(5)
print(float_val)

# str to float
float_val = float("6.77")
print(float_val)

# boolean to float
float_val = float(True)
print(float_val)
float_val = float(False)
print(float_val)


