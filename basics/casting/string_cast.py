# str(x): Converts x to a string.
# This works on almost any type of object, converting numbers, lists, and other types into their string representations.

# float to String
str_val = str(5.5)
print(str_val)

# int to String
str_val = str(58)
print(str_val)

# boolean to String
str_val = str(True)
print(str_val)
str_val = str(False)
print(str_val)
